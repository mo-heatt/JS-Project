let input = prompt("Enter season: ");
switch(input){
    case "September":
    case "October":
    case "November":
        alert("AUTUMN");
        break;
    case "December":
    case "January":
    case "February":
        alert("WINTER");
        break;
    case "March":
    case "April":
    case "May":
        alert("SPRING");
        break;
    case "June":
    case "July":
    case "August":
        alert("SUMMER");
        break;
}